package com.green.codingtest.srp.service.model;

/**
 * <h2>class Result</h2>
 *
 * Enum representing possible game results
 *
 * @author <a href="mailto:greenomsk@gmail.com">Andrey Grinenko</a>
 * @since 23.07.2019
 */
public enum Result {
    /**
     * Player has won
     */
    WIN,
    /**
     * Player has lost
     */
    LOSE,
    /**
     * Draw result, i.e. neither win nor lose
     */
    DRAW
}
